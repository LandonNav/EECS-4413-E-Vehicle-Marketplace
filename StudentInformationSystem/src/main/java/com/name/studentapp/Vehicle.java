package com.name.studentapp;

public class Vehicle { 

    private int id; 

    private String make; 

    private int year; 

    private String model; 

  

    // Getters 

    public int getId() { 

        return id; 

    } 

  

    public String getMake() { 

        return make; 

    } 

  

    public int getYear() { 

        return year; 

    } 

  

    public String getModel() { 

        return model; 

    } 

  

    // Setters 

    public void setId(int id) { 

        this.id = id; 

    } 

  

    public void setMake(String name) { 

        this.make = name; 

    } 

  

    public void setYear(int age) { 

        this.year = age; 

    } 

  

    public void setModel(String major) { 

        this.model = major; 

    } 

} 